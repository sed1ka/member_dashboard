  static const double headerLarge = 29;
  static const double headerMedium = 26;
  static const double headerSmall = 23;

  static const double titleLarge = 20;
  static const double titleMedium = 18;
  static const double titleSmall = 16;

  static const double bodyLarge = 14.5;
  static const double bodyMedium = 13.5;
  static const double bodySmall = 12;

  static const double subtitleLarge = 11;
  static const double subtitleMedium = 10;
  static const double subtitleSmall = 9;
}

/// ICON SIZE PROPERTIES
